// Declaration and Initialization of two variables
let a = 5;
let b = 2;

// Several examples of operations on variables 

console.log('a = ', + a);
console.log('b = ', + b);

console.log('a + b = ' + (a + b));
console.log('a - b = ', + (a - b));
console.log('a * b = ', + (a * b));
console.log('a / b = ', + (a / b));
console.log('a % b = ', + (a % b));

console.log('a++ = ', + (a++));
console.log('--b = ', + (--b));

console.log('a = ', + a);
console.log('b = ', + b);